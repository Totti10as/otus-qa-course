class Math(object):

    def __init__(self, num1, num2):
        self.num1 = num1
        self.num2 = num2

    def sum_nums(self):
        """
        :type nums are: int
        :param num1:
        :param num2:
        :return sum of params:
        """
        return self.num1 + self.num2

    def sub_nums(self):
        """
        :type nums are: int
        :param num1:
        :param num2:
        :return sum of params:
        """
        return self.num1 - self.num2

